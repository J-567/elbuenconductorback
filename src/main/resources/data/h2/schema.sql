DROP TABLE IF EXISTS TRAYECTOS;
DROP TABLE IF EXISTS VEHICULOS;
DROP TABLE IF EXISTS CLIENTES;


CREATE TABLE TRAYECTOS (
	ID				BIGINT			NOT NULL,
	DNI				VARCHAR(9)		NOT NULL,
	MATRICULA		VARCHAR(7)		NOT NULL,
	KM_RECORRIDOS	DOUBLE(10)		NOT NULL,    
	N_ACELERONES	INT				NOT NULL,
	N_FRENAZOS		INT				NOT NULL,
	RPM_MEDIAS		DOUBLE(10)		NOT NULL,
	INICIO			TIMESTAMP		NOT NULL,
	FIN				TIMESTAMP		NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE VEHICULOS (
	MATRICULA				VARCHAR(7)		NOT NULL,
	MARCA					VARCHAR(20)		NOT NULL,
	MODELO					VARCHAR(20)		NOT NULL,
	COLOR					VARCHAR(10)		NOT NULL,    
	FECHA_MATRICULACION		TIMESTAMP		NOT NULL,
	COMBUSTIBLE				VARCHAR(10)		NOT NULL,
	COEFICIENTE				DOUBLE(10)		NOT NULL,
	PRIMARY KEY (MATRICULA)
);

CREATE TABLE CLIENTES (
	DNI						VARCHAR(9)		NOT NULL,
	NOMBRE					VARCHAR(20)		NOT NULL,
	APELLIDO1				VARCHAR(20)		NOT NULL,
	APELLIDO2				VARCHAR(10)		NOT NULL,    
	ZIP_CODE				VARCHAR(10)		NOT NULL,
	FECHA_NACIMIENTO		TIMESTAMP		NOT NULL,
	FECHA_CARNET			TIMESTAMP		NOT NULL,
	CALIDAD_CONDUCCION		INT				NOT NULL,
	PRIMARY KEY (DNI)
);
